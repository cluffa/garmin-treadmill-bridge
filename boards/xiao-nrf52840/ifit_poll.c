static const uint8_t START_04[] = {0xfe,0x02,0x11,0x02};
static const uint8_t START_05[] = {0xff,0x11,0x02,0x04,0x02,0x0d,0x04,0x0d,0x02,0x02,0x03,0x10,0xa0,0x00,0x00,0x00,0x0a,0x00,0xd2,0x00};
